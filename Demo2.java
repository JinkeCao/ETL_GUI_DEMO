class MyCustomFilter extends javax.swing.filechooser.FileFilter {

    @Override
    public boolean accept(File file) {
        // Allow only directories, or files with ".txt" extension
        return file.isDirectory()
                || file.getAbsolutePath().endsWith(".txt")
                || file.getAbsolutePath().endsWith(".avro")
                || file.getAbsolutePath().endsWith(".json")
                || file.getAbsolutePath().endsWith(".text")
                || file.getAbsolutePath().endsWith(".seqjson")
                || file.getAbsolutePath().endsWith(".seqfile");
    }

    @Override
    public String getDescription() {
        // This description will be displayed in the dialog,
        // hard-coded = ugly, should be done via I18N
        return "(*.txt) (*.avro) (*.json) (*.text) (*.seqjson) (*.seqfile)";
    }
}

